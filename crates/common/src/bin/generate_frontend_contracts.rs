use std::{
    env, fs,
    io::{self, Write},
    path::{Path, PathBuf},
};

use vpsman_common::{
    job_command_variant_names, job_privilege_intent_fields, schedule_privilege_intent_fields,
    CURRENT_COMMAND_PROTOCOL_VERSION, MAX_TERMINAL_INPUT_BYTES, MIN_TERMINAL_COLS,
    MIN_TERMINAL_ROWS,
};

fn main() -> io::Result<()> {
    let output_path = env::args()
        .nth(1)
        .map(PathBuf::from)
        .unwrap_or_else(|| PathBuf::from("frontend/src/generated/protocolContracts.ts"));
    if let Some(parent) = output_path.parent() {
        fs::create_dir_all(parent)?;
    }
    let mut output = Vec::new();
    writeln!(
        output,
        "// @generated by cargo run -p vpsman-common --bin generate_frontend_contracts"
    )?;
    writeln!(output, "// Do not edit by hand.\n")?;
    writeln!(
        output,
        "export const COMMAND_PROTOCOL_VERSION = {} as const;",
        CURRENT_COMMAND_PROTOCOL_VERSION
    )?;
    writeln!(
        output,
        "export const MIN_TERMINAL_COLS = {} as const;",
        MIN_TERMINAL_COLS
    )?;
    writeln!(
        output,
        "export const MIN_TERMINAL_ROWS = {} as const;",
        MIN_TERMINAL_ROWS
    )?;
    writeln!(
        output,
        "export const MAX_TERMINAL_INPUT_BYTES = {} as const;",
        MAX_TERMINAL_INPUT_BYTES
    )?;
    write_string_array(
        &mut output,
        "JOB_OPERATION_TYPES",
        job_command_variant_names(),
    )?;
    writeln!(
        output,
        "export type GeneratedJobOperationType = typeof JOB_OPERATION_TYPES[number];"
    )?;
    write_string_array(
        &mut output,
        "JOB_PRIVILEGE_INTENT_FIELDS",
        job_privilege_intent_fields(),
    )?;
    write_string_array(
        &mut output,
        "SCHEDULE_PRIVILEGE_INTENT_FIELDS",
        schedule_privilege_intent_fields(),
    )?;
    atomic_write(&output_path, &output)
}

fn write_string_array(output: &mut Vec<u8>, name: &str, values: &[&str]) -> io::Result<()> {
    writeln!(output, "\nexport const {name} = [")?;
    for value in values {
        writeln!(output, "  {value:?},")?;
    }
    writeln!(output, "] as const;")
}

fn atomic_write(path: &Path, contents: &[u8]) -> io::Result<()> {
    let tmp_path = path.with_extension("tmp");
    fs::write(&tmp_path, contents)?;
    fs::rename(tmp_path, path)
}
