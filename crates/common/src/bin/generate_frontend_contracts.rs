fn write_domain_array(
    output: &mut Vec<u8>,
    name: &str,
    type_name: &str,
    values: &[&str],
) -> io::Result<()> {
    write_string_array(output, name, values)?;
    writeln!(output, "export type {type_name} = typeof {name}[number];")
}

fn write_status_class_map(
    output: &mut Vec<u8>,
    name: &str,
    values: &[(&str, &str)],
    key_type: &str,
) -> io::Result<()> {
    write_string_map(
        output,
        name,
        values,
        key_type,
        "GeneratedWorkflowStatusClass",
    )
}

fn write_string_array(output: &mut Vec<u8>, name: &str, values: &[&str]) -> io::Result<()> {
    writeln!(output, "\nexport const {name} = [")?;
    for value in values {
        writeln!(output, "  {value:?},")?;
    }
    writeln!(output, "] as const;")
}

fn write_string_map(
    output: &mut Vec<u8>,
    name: &str,
    values: &[(&str, &str)],
    key_type: &str,
    value_type: &str,
) -> io::Result<()> {
    writeln!(output, "\nexport const {name} = {{")?;
    for (key, value) in values {
        writeln!(output, "  {key:?}: {value:?},")?;
    }
    writeln!(
        output,
        "}} as const satisfies Record<{key_type}, {value_type}>;"
    )
}

fn write_bool_map(
    output: &mut Vec<u8>,
    name: &str,
    values: &[(&str, bool)],
    key_type: &str,
) -> io::Result<()> {
    writeln!(output, "\nexport const {name} = {{")?;
    for (key, value) in values {
        writeln!(output, "  {key:?}: {value},")?;
    }
    writeln!(output, "}} as const satisfies Record<{key_type}, boolean>;")
}

